SelectedCrayon::SelectedCrayon(rgb_color color)
	:
	Crayon(color)
{
	SetExplicitMaxSize(BSize(kCrayonWidth - 1, kCrayonHeight - 1));
}


SelectedCrayon::~SelectedCrayon()
{
}


void
SelectedCrayon::AttachedToWindow()
{
	ResizeTo(kCrayonWidth - 1, kCrayonHeight - 1);
	fIcon = new BBitmap(Bounds(), kCrayonColorSpace, false, false);
}


void
SelectedCrayon::Draw(BRect updateRect)
{
	BRect frame(Bounds());

	PushState();
	SetHighColor(fColor);
	SetLowColor(Parent()->ViewColor());
	FillRect(frame);

	if (fIcon != NULL) {
		bool useFilled = fColor.red + fColor.green + fColor.blue <= 42 * 3;
		fIcon->SetBits(useFilled ? kCrayonFilledBits : kCrayonBits,
			kCrayonWidth * kCrayonHeight, 0, kCrayonColorSpace);
		SetDrawingMode(B_OP_OVER);
		DrawBitmap(fIcon, frame);
	}
	PopState();
}
